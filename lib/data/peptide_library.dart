// PepScore: Research quality scoring (0-100)
class PepScore {
  final int publication;
  final int evidence;
  final int methodology;
  final int relevance;
  
  const PepScore({
    this.publication = 0,
    this.evidence = 0,
    this.methodology = 0,
    this.relevance = 0,
  });
  
  int get overallScore => 
    ((publication * 0.25) + 
     (evidence * 0.35) + 
     (methodology * 0.25) + 
     (relevance * 0.15)).round();
  
  String get rating {
    final score = overallScore;
    if (score >= 80) return 'Excellent';
    if (score >= 60) return 'Good';
    if (score >= 40) return 'Fair';
    return 'Limited';
  }
}

